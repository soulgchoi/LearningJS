// 리터럴
// "값을 만드는 방법"
// 리터럴과 식별자의 차이를 아는 것이 중요하다.

let room1 = "conference_room_a"
// 식별자 = 문자열 리터럴 & room1 의 값
// 식별자는 숫자로 시작할 수 없으므로 숫자형 리터럴에 ""를 붙일 필요가 없다.

let currentRoom = room1;
// currentRoom 의 값은 room1 의 값("conference_room_a")과 같다.

currentRoom = conference_room_a; 
// conference_room_a 라는 식별자가 없으므로 에러