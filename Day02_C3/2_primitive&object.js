// 원시 타입(primitive)과 객체(object)

// primitive 타입
// 숫자 / 문자열 / boolean / null / undefined / symbol
// 원시 타입은 불변(immutable)하다.
let str = "hello";
str = "world";
// "hello" 와 "world" 는 불변하는 서로 다른 문자열이다.

// Object
// Array / Date / RegExp / Map & WeakMap / Set & WeakSet
// Number / String / Boolean
